package com.javaex.practice03;

public class Ex08 {

	public static void main(String[] args) {

		int i;
		int r;
		
		for (i=1; i<=9; i++) {
			for (r=2; r<=9; r++) {
				System.out.print(r+ " * " + i  + " = " + (i*r) + "\t");
			}		
			System.out.println("");
		}
		
		
		
	}

}
